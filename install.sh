function getVariable() {
    local tmp
    prompt="$1"
    default="$2"
    echo -en "${prompt}\n[${default}]: " >&2
    read tmp
    if [ "$tmp" == "" ] ; then
        tmp="$default"
    fi
    echo $tmp
}
function die() {
    echo $* >&2
    echo "Installation aborted.\n"
    exit 1
}
