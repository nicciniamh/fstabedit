function getVariable() {
    local tmp
    var="$1"
    prompt="$2"
    default="$3"
    echo "${prompt}"
    read -p "[${default}]: " tmp
    declare -g $var="$tmp"
    [ "$tmp" == "" ] && declare -g $var="$default"
}
